class Mobil():

    # public variabel
    def __init__(self, jendela, pintu, mesin):
        self.jendela = jendela
        self.pintu = pintu
        self.mesin = mesin

    # protected variabel
    def __init__(self, jendela, pintu, mesin):
        self._jendela = jendela
        self._pintu = pintu
        self._mesin = mesin
    
    # private variabel
    def __init__(self, jendela, pintu, mesin):
        self.__jendela = jendela
        self.__pintu = pintu
        self.__mesin = mesin